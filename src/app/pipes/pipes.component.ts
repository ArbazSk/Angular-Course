import { Component } from '@angular/core';

@Component({
  selector: 'app-pipes',
  templateUrl: './pipes.component.html',
  styleUrls: ['./pipes.component.css']
})
export class PipesComponent {
  servers = [
    {
      instanceType: 'medium',
      name: 'Production Server',
      status: 'stable',
      started: new Date(15, 1, 2017)
    },
    {
      instanceType: 'large',
      name: 'User Database',
      status: 'stable',
      started: new Date(15, 1, 2017)
    },
    {
      instanceType: 'small',
      name: 'Development Server',
      status: 'offline',
      started: new Date(15, 1, 2017)
    },
    {
      instanceType: 'small',
      name: 'Testing Environment Server',
      status: 'stable',
      started: new Date(15, 1, 2017)
    }
  ];
  filterInput: string;

  appStatus = new Promise((resolve, reject) => {
    setTimeout(() => resolve("offline"), 2000)
  });

  // we can use async pipe when dealing with asynchronous data
  // it worls on Promise and Observable

  getStatusClasses(server: { instanceType: string, name: string, status: string, started: Date }) {
    return {
      'list-group-item-success': server.status === 'stable',
      'list-group-item-warning': server.status === 'offline',
      'list-group-item-danger': server.status === 'critical'
    };
  }

  addServer() {
    this.servers.push({
      instanceType: 'small',
      name: 'New Server',
      status: 'stable',
      started: new Date(15, 1, 2017)
    })
  }
  /* 
  Problem with pipes: When data changes i.e property has been added or item has been added
    in an Array then pipes doesn't run every time data changes because of performance issues.
    This is called Impure Pipes. it changes only on pipe input
    To call filter pipe on every changes we need to add property in pipe class Decorator called pure: false
    this will call pipe on every data change.
  
  */
}
